"use client";

import React, { useRef, useState } from 'react';
import { Share2, Download, Twitter, Facebook, Copy, Check } from 'lucide-react';
import Image from 'next/image';
import { teams } from '../data/teams';
import { Team } from '../types';

interface MatchShareCardProps {
    homeTeam: string;
    awayTeam: string;
    homeScore: number;
    awayScore: number;
    date: string;
    venue?: string;
    onClose?: () => void;
}

const MatchShareCard: React.FC<MatchShareCardProps> = ({
    homeTeam,
    awayTeam,
    homeScore,
    awayScore,
    date,
    venue,
    onClose
}) => {
    const cardRef = useRef<HTMLDivElement>(null);
    const [copied, setCopied] = useState(false);

    const homeTeamData = teams.find((t: Team) => t.name === homeTeam);
    const awayTeamData = teams.find((t: Team) => t.name === awayTeam);

    const shareText = `âš½ Match Result\n\n${homeTeam} ${homeScore} - ${awayScore} ${awayTeam}\n\nðŸ“… ${date}\nðŸŸï¸ ${venue || 'Mwiri League'}\n\n#MwiriLeague #Football`;

    const handleCopyText = async () => {
        try {
            await navigator.clipboard.writeText(shareText);
            setCopied(true);
            setTimeout(() => setCopied(false), 2000);
        } catch (err) {
            console.error('Failed to copy:', err);
        }
    };

    const handleTwitterShare = () => {
        const url = encodeURIComponent(window.location.href);
        const text = encodeURIComponent(shareText);
        window.open(`https://twitter.com/intent/tweet?text=${text}&url=${url}`, '_blank');
    };

    const handleFacebookShare = () => {
        const url = encodeURIComponent(window.location.href);
        window.open(`https://www.facebook.com/sharer/sharer.php?u=${url}`, '_blank');
    };

    const handleWhatsAppShare = () => {
        const text = encodeURIComponent(shareText);
        window.open(`https://wa.me/?text=${text}`, '_blank');
    };

    return (
        <div className="fixed inset-0 bg-black/50 flex items-center justify-center z-50 p-4" onClick={onClose}>
            <div
                className="bg-white dark:bg-slate-800 rounded-2xl shadow-2xl max-w-md w-full overflow-hidden"
                onClick={(e) => e.stopPropagation()}
            >
                {/* Share Card Preview */}
                <div
                    ref={cardRef}
                    className="bg-gradient-to-br from-mwiri-blue-deep via-mwiri-blue to-mwiri-blue-dark p-8"
                >
                    <div className="text-center mb-4">
                        <span className="text-xs font-bold text-mwiri-yellow uppercase tracking-widest">
                            Full Time
                        </span>
                    </div>

                    <div className="flex items-center justify-between gap-4">
                        {/* Home Team */}
                        <div className="flex-1 text-center">
                            <div className="w-16 h-16 mx-auto mb-2 bg-white/10 rounded-full p-2 flex items-center justify-center">
                                {homeTeamData?.logo ? (
                                    <Image
                                        src={homeTeamData.logo}
                                        alt={homeTeam}
                                        width={48}
                                        height={48}
                                        className="object-contain"
                                    />
                                ) : (
                                    <span className="text-2xl font-black text-white/50">
                                        {homeTeam.charAt(0)}
                                    </span>
                                )}
                            </div>
                            <p className="text-white font-bold text-sm truncate">{homeTeam}</p>
                        </div>

                        {/* Score */}
                        <div className="flex items-center gap-3">
                            <span className="text-5xl font-black text-white">{homeScore}</span>
                            <span className="text-2xl font-bold text-white/50">-</span>
                            <span className="text-5xl font-black text-white">{awayScore}</span>
                        </div>

                        {/* Away Team */}
                        <div className="flex-1 text-center">
                            <div className="w-16 h-16 mx-auto mb-2 bg-white/10 rounded-full p-2 flex items-center justify-center">
                                {awayTeamData?.logo ? (
                                    <Image
                                        src={awayTeamData.logo}
                                        alt={awayTeam}
                                        width={48}
                                        height={48}
                                        className="object-contain"
                                    />
                                ) : (
                                    <span className="text-2xl font-black text-white/50">
                                        {awayTeam.charAt(0)}
                                    </span>
                                )}
                            </div>
                            <p className="text-white font-bold text-sm truncate">{awayTeam}</p>
                        </div>
                    </div>

                    <div className="text-center mt-6 pt-4 border-t border-white/20">
                        <p className="text-blue-200 text-xs font-medium">{date}</p>
                        <p className="text-mwiri-yellow text-sm font-bold mt-1">Mwiri League</p>
                    </div>
                </div>

                {/* Share Options */}
                <div className="p-6">
                    <h3 className="text-lg font-bold text-gray-900 dark:text-white mb-4 flex items-center gap-2">
                        <Share2 className="w-5 h-5" />
                        Share Result
                    </h3>

                    <div className="grid grid-cols-4 gap-3 mb-6">
                        <button
                            onClick={handleTwitterShare}
                            className="flex flex-col items-center gap-2 p-3 rounded-xl bg-gray-50 dark:bg-slate-700 hover:bg-blue-50 dark:hover:bg-blue-900/30 transition-colors"
                        >
                            <Twitter className="w-6 h-6 text-blue-400" />
                            <span className="text-xs font-medium text-gray-600 dark:text-gray-400">Twitter</span>
                        </button>
                        <button
                            onClick={handleFacebookShare}
                            className="flex flex-col items-center gap-2 p-3 rounded-xl bg-gray-50 dark:bg-slate-700 hover:bg-blue-50 dark:hover:bg-blue-900/30 transition-colors"
                        >
                            <Facebook className="w-6 h-6 text-blue-600" />
                            <span className="text-xs font-medium text-gray-600 dark:text-gray-400">Facebook</span>
                        </button>
                        <button
                            onClick={handleWhatsAppShare}
                            className="flex flex-col items-center gap-2 p-3 rounded-xl bg-gray-50 dark:bg-slate-700 hover:bg-green-50 dark:hover:bg-green-900/30 transition-colors"
                        >
                            <svg className="w-6 h-6 text-green-500" fill="currentColor" viewBox="0 0 24 24">
                                <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
                            </svg>
                            <span className="text-xs font-medium text-gray-600 dark:text-gray-400">WhatsApp</span>
                        </button>
                        <button
                            onClick={handleCopyText}
                            className="flex flex-col items-center gap-2 p-3 rounded-xl bg-gray-50 dark:bg-slate-700 hover:bg-gray-100 dark:hover:bg-slate-600 transition-colors"
                        >
                            {copied ? (
                                <Check className="w-6 h-6 text-green-500" />
                            ) : (
                                <Copy className="w-6 h-6 text-gray-500" />
                            )}
                            <span className="text-xs font-medium text-gray-600 dark:text-gray-400">
                                {copied ? 'Copied!' : 'Copy'}
                            </span>
                        </button>
                    </div>

                    <button
                        onClick={onClose}
                        className="w-full py-3 text-center text-gray-600 dark:text-gray-400 hover:text-gray-900 dark:hover:text-white font-medium transition-colors"
                    >
                        Close
                    </button>
                </div>
            </div>
        </div>
    );
};

export default MatchShareCard;
